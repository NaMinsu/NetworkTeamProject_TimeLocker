# Term Project Database

DROP DATABASE TIMELOCKER_DB;
CREATE DATABASE TIMELOCKER_DB;
USE TIMELOCKER_DB;

CREATE TABLE USERS (
ID VARCHAR(20) PRIMARY KEY,
PASSWORD VARCHAR(20) NOT NULL,
NAME VARCHAR(20) NOT NULL,
AGE INT DEFAULT 1,
PHONENUM NUMERIC(11,0),
POINT INT DEFAULT 0);

CREATE TABLE PCROOM (
ID NUMERIC(8,0) PRIMARY KEY,
NAME VARCHAR(20) NOT NULL,
MANAGER VARCHAR(20),
DO VARCHAR(20),
SI VARCHAR(20),
GU VARCHAR(20),
DONG VARCHAR(20),
FEE INT DEFAULT 0);

CREATE TABLE REGISTERED (
PRC_ID NUMERIC(8,0) REFERENCES PCROOM(ID),
USER_ID VARCHAR(20) REFERENCES USER(ID),
LEFTTIME INT DEFAULT 0);

COMMIT;
